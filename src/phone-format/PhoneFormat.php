    public function reformat($phone='')
    {

        foreach ($this->phone as $value) {
            $count = ( strlen($value) == 2 ) ? 4 : 3;
            $re = '/^('.$value.')([0-9]{'.$count.'})([0-9]{3})(.*)/m';
            $str = $phone;
            preg_match_all($re, $str, $matches, PREG_SET_ORDER, 0);

            if (! empty( $matches )) {
                echo '('.$matches[1].') '.$matches[2].' '.$matches[3].' '.$matches[4];
                die();
            }
        }



    }
}